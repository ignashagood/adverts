CREATE TABLE ROLE (
     ID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
     NAME VARCHAR(50) NOT NULL,
     ADVERT_PAGE BOOLEAN NOT NULL,
     CHANGING_CITY BOOLEAN NOT NULL,
     SENDING_APPLICATION BOOLEAN NOT NULL,
     APPLICATIONS_LIST BOOLEAN NOT NULL,
     APPLICATION_PAGE BOOLEAN NOT NULL,
     AGREEMENT BOOLEAN NOT NULL,
     APPEND_ADVERT BOOLEAN NOT NULL,
     EDIT_ADVERT BOOLEAN NOT NULL
);