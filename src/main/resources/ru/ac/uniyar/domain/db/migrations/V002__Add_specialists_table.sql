CREATE TABLE SPECIALISTS (
    SPECIALIST_ID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    LOCALE VARCHAR(50) NOT NULL,
    INITIALS VARCHAR(50) NOT NULL,
    EDUCATION VARCHAR(100) NOT NULL ,
    PHONE_NUMBER VARCHAR(20) NOT NULL,
    CERTIFICATES VARCHAR(20) NOT NULL,
    WORK_EXPERIENCE VARCHAR(20) NOT NULL,
    DETAIL_CONTACTS VARCHAR(20) NOT NULL,
    LOGIN VARCHAR(20) NOT NULL UNIQUE,
    PASSWORD BINARY VARYING(64),
    CITY INT NOT NULL,
    ROLE_ID VARCHAR(2) NOT NULL,
    CONSTRAINT FK_SPECIALIST_ROLE FOREIGN KEY (ROLE_ID) REFERENCES ROLE(ID)
);
