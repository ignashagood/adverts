CREATE TABLE ADVERTS (
    ADVERT_ID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    SERVICE_ID INT NOT NULL,
    CONSTRAINT ADVERTS_SERVICES_FK FOREIGN KEY (SERVICE_ID)
        REFERENCES SERVICES (SERVICE_ID)
            ON DELETE CASCADE
            ON UPDATE CASCADE,
    ADVERT_NAME VARCHAR(50) NOT NULL,
    CITY_ID INT NOT NULL,
    CONSTRAINT ADVERTS_CITIES_FK FOREIGN KEY (CITY_ID)
        REFERENCES CITIES (CITY_ID)
            ON DELETE CASCADE
            ON UPDATE CASCADE,
    DESCRIPTION VARCHAR(100) NOT NULL ,
    SPECIALIST_ID INT NOT NULL,
    APPLICATION_ID INT NOT NULL ,
    CONSTRAINT ADVERTS_APPLICATIONS_FK FOREIGN KEY (APPLICATION_ID)
        REFERENCES APPLICATIONS (ID)
        ON DELETE CASCADE
        ON UPDATE CASCADE,
    CONSTRAINT ADVERTS_SPECIALISTS_FK FOREIGN KEY (SPECIALIST_ID)
        REFERENCES SPECIALISTS (SPECIALIST_ID)
            ON DELETE CASCADE
            ON UPDATE CASCADE
);